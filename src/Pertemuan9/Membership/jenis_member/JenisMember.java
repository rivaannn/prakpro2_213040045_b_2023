package Pertemuan9.Membership.jenis_member;

public class JenisMember {
    private String id;
    private String nama;

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return this.id;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNama() {
        return this.nama;
    }

}
